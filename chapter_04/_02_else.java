package chapter_04;

public class _02_else {
    public static void main(String[] args) {
        int hour = 14;
        if (hour < 14) {
            System.out.println("아이스 아메리카노 +1");
        } else {
            System.out.println("디카페인 아이스 아메리카노 +1");
        }
        System.out.println("커피 주문 완료");
    }
}
